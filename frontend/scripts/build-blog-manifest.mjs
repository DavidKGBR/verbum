// Generate src/blog/manifest.ts at build time.
//
// Reads frontmatter from every src/blog/posts/*.mdx and emits a typed list
// the BlogIndexPage can iterate over without running MDX itself. Posts are
// imported lazily by slug via dynamic import in BlogPostPage.

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import matter from "gray-matter";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const POSTS_DIR = path.resolve(__dirname, "../src/blog/posts");
const MANIFEST_PATH = path.resolve(__dirname, "../src/blog/manifest.ts");

if (!fs.existsSync(POSTS_DIR)) {
  fs.mkdirSync(POSTS_DIR, { recursive: true });
}

const files = fs.readdirSync(POSTS_DIR).filter((f) => f.endsWith(".mdx"));
const posts = files
  .map((file) => {
    const raw = fs.readFileSync(path.join(POSTS_DIR, file), "utf8");
    const { data } = matter(raw);
    if (!data.slug || !data.title) {
      throw new Error(`[blog-manifest] ${file}: missing slug or title in frontmatter`);
    }
    return {
      slug: data.slug,
      title: data.title,
      description: data.description ?? "",
      date: data.date ?? new Date().toISOString().split("T")[0],
      ogImage: data.ogImage ?? "/og-default.png",
      tags: data.tags ?? [],
      file: file.replace(/\.mdx$/, ""),
    };
  })
  .sort((a, b) => (a.date < b.date ? 1 : -1));

const ts =
  `// AUTO-GENERATED by scripts/build-blog-manifest.mjs — do not edit by hand.\n` +
  `// Re-run \`npm run prebuild\` (or \`npm run build\`) to refresh.\n\n` +
  `export interface BlogPost {\n` +
  `  slug: string;\n` +
  `  title: string;\n` +
  `  description: string;\n` +
  `  date: string;\n` +
  `  ogImage: string;\n` +
  `  tags: string[];\n` +
  `  file: string;\n` +
  `}\n\n` +
  `export const POSTS: BlogPost[] = ${JSON.stringify(posts, null, 2)};\n`;

fs.mkdirSync(path.dirname(MANIFEST_PATH), { recursive: true });
fs.writeFileSync(MANIFEST_PATH, ts, "utf8");
console.log(`[blog-manifest] wrote ${posts.length} posts to src/blog/manifest.ts`);
