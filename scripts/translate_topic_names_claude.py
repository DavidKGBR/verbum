def main():
    conn = duckdb.connect(DB_PATH)

    remaining = conn.execute("""
        SELECT t.topic_id, t.name
        FROM topics t
        LEFT JOIN topics_multilang m ON t.topic_id = m.topic_id AND m.lang = 'pt'
        WHERE m.topic_id IS NULL
        ORDER BY t.name
    """).fetchall()
    print(f"Untranslated topics: {len(remaining)}")

    all_maps = {**CONCEPTS, **PROPER_NAMES}

    rows = []
    matched = 0
    kept_as_is = 0
    for tid, name in remaining:
        key = name.upper().strip()
        if key in all_maps:
            pt, es = all_maps[key]
            matched += 1
        else:
            # Keep as-is (most obscure biblical names are identical across languages)
            pt, es = key, key
            kept_as_is += 1
        rows.append((str(tid), "pt", pt, 0.85))
        rows.append((str(tid), "es", es, 0.85))

    print(f"Matched from dictionaries: {matched}")
    print(f"Kept as-is (obscure names): {kept_as_is}")

    conn.executemany(
        """INSERT INTO topics_multilang (topic_id, lang, name, confidence)
           VALUES (?, ?, ?, ?)
           ON CONFLICT (topic_id, lang) DO UPDATE SET
             name = EXCLUDED.name,
             confidence = EXCLUDED.confidence""",
        rows,
    )

    total_pt = conn.execute(
        "SELECT COUNT(*) FROM topics_multilang WHERE lang = 'pt'"
    ).fetchone()[0]
    total_es = conn.execute(
        "SELECT COUNT(*) FROM topics_multilang WHERE lang = 'es'"
    ).fetchone()[0]
    total_topics = conn.execute("SELECT COUNT(*) FROM topics").fetchone()[0]
    print(f"\nFinal: PT {total_pt}/{total_topics}, ES {total_es}/{total_topics}")
    conn.close()


if __name__ == "__main__":
    main()
