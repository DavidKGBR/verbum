def translate_topic(name: str, known: dict[str, str]) -> str | None:
    up = name.upper().strip()
    if up in known:
        return known[up]

    # Try without trailing "(N)" like "SIN (1)"
    base = re.sub(r"\s*\(\d+\)\s*$", "", up).strip()
    suffix = up[len(base):].strip()
    if base in known:
        return f"{known[base]} {suffix}".strip() if suffix else known[base]

    return None


